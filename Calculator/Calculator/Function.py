def add(num1, num2):
    return num1 + num2

def substract(num1, num2):
    return num1 - num2

def multiply(num1, num2):
    return num1 * num2

def divide(num1, num2):
    if num1 != 0:
        return num1 / num2
    else:
        return "Error: Cannot Divide By Zero."
        
    